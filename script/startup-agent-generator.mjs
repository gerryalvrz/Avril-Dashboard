#!/usr/bin/env node

import fs from 'node:fs/promises';
import path from 'node:path';
import os from 'node:os';
import process from 'node:process';
import { ethers } from 'ethers';

const DEFAULT_KEYS_DIR = path.join(os.homedir(), '.config', 'prism', 'keys');
const DEFAULT_RPC_URL = process.env.CELO_RPC_URL || process.env.NEXT_PUBLIC_CELO_RPC_URL || 'https://forno.celo.org';
const DEFAULT_CHAIN_ID = Number(process.env.CELO_CHAIN_ID || 42220);
const DEFAULT_FUNDING = process.env.STARTUP_AGENT_DEFAULT_FUNDING_CELO || '0.01';
const ERC8004_REGISTRY = process.env.ERC8004_REGISTRY_ADDRESS || '0x8004A169FB4a3325136EB29fA0ceB6D2e539a432';
const PRISM_FACTORY_ADDRESS = process.env.PRISM_FACTORY_ADDRESS || '';
const PRISM_CONTEXT_ADDRESS = process.env.PRISM_CONTEXT_ADDRESS || '';
const ENS_REGISTRY_ADDRESS = process.env.ENS_REGISTRY_ADDRESS || '';

const FACTORY_ABI = [
  'function createContext(address root,address delegate,uint256 spendingLimit,uint256 dailyLimit,uint256 ttl,address[] allowlist) returns (address)',
];
const CONTEXT_ABI = [
  'function execute(address target,uint256 value,bytes data) returns (bytes)',
  'function revokeDelegate(address delegate)',
];
const ENS_REGISTRY_ABI = ['function setSubnodeRecord(bytes32 node,bytes32 label,address owner,address resolver,uint64 ttl)'];
const ERC8004_ABI = ['function register(string agentURI) returns (uint256)'];

function sanitizeLabel(input) {
  const out = String(input || '')
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9-]/g, '-')
    .replace(/-+/g, '-')
    .replace(/^-|-$/g, '');
  if (!out) throw new Error(`Invalid label: "${input}"`);
  return out;
}

function normalizeRoles(rawRoles) {
  const parsed = JSON.parse(rawRoles);
  if (!Array.isArray(parsed) || parsed.length === 0) {
    throw new Error('roles-json must be a non-empty JSON array.');
  }
  return parsed.map((entry, index) => {
    if (typeof entry === 'string') {
      return {
        role: sanitizeLabel(entry),
        policy: { spendingLimit: '0', dailyLimit: '0', ttl: 86400, allowlist: [] },
      };
    }
    if (!entry || typeof entry !== 'object') throw new Error(`roles-json[${index}] must be an object or string.`);
    const role = sanitizeLabel(entry.role || `agent-${index + 1}`);
    const policy = entry.policy || {};
    return {
      role,
      policy: {
        spendingLimit: String(policy.spendingLimit ?? '0'),
        dailyLimit: String(policy.dailyLimit ?? '0'),
        ttl: Number(policy.ttl ?? 86400),
        allowlist: Array.isArray(policy.allowlist) ? policy.allowlist : [],
      },
    };
  });
}

async function ensureDir(dir, mode = 0o700) {
  await fs.mkdir(dir, { recursive: true, mode });
}

function toHexHash(seed) {
  return ethers.keccak256(ethers.toUtf8Bytes(seed));
}

function pseudoAddress(seed) {
  return ethers.getAddress(`0x${toHexHash(seed).slice(-40)}`);
}

async function loadRootWallet(provider) {
  const rootPk = process.env.PRISM_ROOT_PRIVATE_KEY;
  if (rootPk) return new ethers.Wallet(rootPk, provider);

  const rootPath = process.env.PRISM_ROOT_KEY_PATH || path.join(DEFAULT_KEYS_DIR, 'root-wallet.json');
  const raw = await fs.readFile(rootPath, 'utf8').catch(() => {
    throw new Error(`Missing root key: set PRISM_ROOT_PRIVATE_KEY or create ${rootPath}.`);
  });
  const parsed = JSON.parse(raw);
  if (!parsed.privateKey) throw new Error(`Invalid root key file at ${rootPath}: missing privateKey.`);
  return new ethers.Wallet(parsed.privateKey, provider);
}

async function writeDelegateKey(startup, role, wallet) {
  const keysDir = process.env.PRISM_KEYS_DIR || DEFAULT_KEYS_DIR;
  await ensureDir(keysDir);
  const outPath = path.join(keysDir, `${sanitizeLabel(startup)}-${sanitizeLabel(role)}.json`);
  const payload = {
    address: wallet.address,
    privateKey: wallet.privateKey,
    createdAt: new Date().toISOString(),
    note: 'Delegate key generated by startup-agent-generator',
  };
  await fs.writeFile(outPath, JSON.stringify(payload, null, 2), { mode: 0o600 });
  return outPath;
}

function classifyError(error) {
  const msg = String(error?.shortMessage || error?.message || error || '');
  if (/insufficient funds/i.test(msg)) return 'insufficient_funds';
  if (/nonce/i.test(msg)) return 'nonce_error';
  if (/gas|required exceeds allowance|intrinsic gas too low/i.test(msg)) return 'gas_error';
  if (/revert|execution reverted|CALL_EXCEPTION/i.test(msg)) return 'revert';
  return 'unknown';
}

async function safeSend(label, fn) {
  try {
    const result = await fn();
    return { ok: true, label, ...result };
  } catch (error) {
    return {
      ok: false,
      label,
      errorType: classifyError(error),
      errorMessage: String(error?.shortMessage || error?.message || error),
    };
  }
}

async function createContext({ rootWallet, role, policy, provider, startup }) {
  const delegate = ethers.Wallet.createRandom();
  const keyPath = await writeDelegateKey(startup, role, delegate);
  const txs = [];

  let contextAddress = pseudoAddress(`${startup}:${role}:context`);
  const creation = await safeSend(`${role}:context:create`, async () => {
    if (!PRISM_FACTORY_ADDRESS) {
      return { txHash: null, contextAddress, simulated: true };
    }
    const factory = new ethers.Contract(PRISM_FACTORY_ADDRESS, FACTORY_ABI, rootWallet);
    const tx = await factory.createContext(
      rootWallet.address,
      delegate.address,
      policy.spendingLimit,
      policy.dailyLimit,
      policy.ttl,
      policy.allowlist
    );
    const receipt = await tx.wait();
    const log = receipt?.logs?.[0];
    if (log && log.address) contextAddress = ethers.getAddress(log.address);
    return { txHash: tx.hash, contextAddress };
  });
  txs.push(creation);

  const funding = await safeSend(`${role}:delegate:fund`, async () => {
    if (!rootWallet || !provider) return { txHash: null, simulated: true };
    const tx = await rootWallet.sendTransaction({ to: delegate.address, value: ethers.parseEther(DEFAULT_FUNDING) });
    await tx.wait();
    return { txHash: tx.hash, amount: DEFAULT_FUNDING };
  });
  txs.push(funding);

  const execution = await safeSend(`${role}:context:execute`, async () => {
    if (!PRISM_CONTEXT_ADDRESS) return { txHash: null, simulated: true };
    const ctx = new ethers.Contract(PRISM_CONTEXT_ADDRESS, CONTEXT_ABI, rootWallet);
    const tx = await ctx.execute(delegate.address, 0n, '0x');
    await tx.wait();
    return { txHash: tx.hash };
  });
  txs.push(execution);

  const revoke = await safeSend(`${role}:context:revoke`, async () => {
    if (!PRISM_CONTEXT_ADDRESS) return { txHash: null, simulated: true };
    const ctx = new ethers.Contract(PRISM_CONTEXT_ADDRESS, CONTEXT_ABI, rootWallet);
    const tx = await ctx.revokeDelegate(delegate.address);
    await tx.wait();
    return { txHash: tx.hash };
  });
  txs.push(revoke);

  return {
    role,
    delegateEoa: delegate.address,
    delegateKeyPath: keyPath,
    contextWallet: contextAddress,
    policy,
    txs,
  };
}

function labelHash(label) {
  return ethers.keccak256(ethers.toUtf8Bytes(label));
}

async function assignSubdomain({ startup, role, contextWallet, rootWallet }) {
  const startupLabel = sanitizeLabel(startup);
  const roleLabel = sanitizeLabel(role);
  const fullName = `${roleLabel}.${startupLabel}.prism-protocol.eth`;
  const fallback = `${roleLabel}.prism-protocol.eth`;
  const parentNode = ethers.namehash(`prism-protocol.eth`);
  const subNode = labelHash(`${roleLabel}.${startupLabel}`);
  const fallbackNode = labelHash(roleLabel);

  const payload = {
    registry: ENS_REGISTRY_ADDRESS || null,
    parentNode,
    labelHash: subNode,
    owner: contextWallet,
    resolver: ethers.ZeroAddress,
    ttl: 300,
    fullName,
    fallback,
  };

  const assigned = await safeSend(`${role}:ens:assign`, async () => {
    if (!ENS_REGISTRY_ADDRESS) return { txHash: null, simulated: true, assignedName: fullName };
    const registry = new ethers.Contract(ENS_REGISTRY_ADDRESS, ENS_REGISTRY_ABI, rootWallet);
    const tx = await registry.setSubnodeRecord(parentNode, subNode, contextWallet, ethers.ZeroAddress, 300);
    await tx.wait();
    return { txHash: tx.hash, assignedName: fullName };
  });

  if (!assigned.ok) {
    const fallbackResult = await safeSend(`${role}:ens:fallback`, async () => {
      if (!ENS_REGISTRY_ADDRESS) return { txHash: null, simulated: true, assignedName: fallback };
      const registry = new ethers.Contract(ENS_REGISTRY_ADDRESS, ENS_REGISTRY_ABI, rootWallet);
      const tx = await registry.setSubnodeRecord(parentNode, fallbackNode, contextWallet, ethers.ZeroAddress, 300);
      await tx.wait();
      return { txHash: tx.hash, assignedName: fallback };
    });
    return { payload, result: fallbackResult };
  }
  return { payload, result: assigned };
}

async function maybeRegister8004({ contextWallet, startup, role, provider }) {
  const doRegister = process.env.STARTUP_AGENT_REGISTER_8004 === '1';
  if (!doRegister) return { requested: false, result: null };

  const uriBase = process.env.STARTUP_AGENT_8004_URI_BASE || process.env.NEXT_PUBLIC_APP_URL || 'https://app.avril.life';
  const uri = `${uriBase.replace(/\/$/, '')}/agent-registration.json?startup=${encodeURIComponent(startup)}&role=${encodeURIComponent(role)}&context=${contextWallet}`;
  const wallet = await loadRootWallet(provider);
  const result = await safeSend(`${role}:erc8004:register`, async () => {
    const registry = new ethers.Contract(ERC8004_REGISTRY, ERC8004_ABI, wallet);
    const tx = await registry.register(uri);
    await tx.wait();
    return { txHash: tx.hash, uri };
  });
  return { requested: true, result };
}

function txHashMap(entries) {
  const out = {};
  for (const t of entries || []) out[t.label] = t.ok ? t.txHash : null;
  return out;
}

async function main() {
  const startup = sanitizeLabel(process.argv[2] || '');
  const rolesJson = process.argv[3] || '';
  if (!startup || !rolesJson) {
    throw new Error('Usage: node script/startup-agent-generator.mjs "<startup-name>" "<roles-json>"');
  }

  const roles = normalizeRoles(rolesJson);
  const provider = new ethers.JsonRpcProvider(DEFAULT_RPC_URL, DEFAULT_CHAIN_ID, { staticNetwork: true });
  const rootWallet = await loadRootWallet(provider);

  const generated = [];
  for (const roleConfig of roles) {
    const base = await createContext({ rootWallet, role: roleConfig.role, policy: roleConfig.policy, provider, startup });
    const ens = await assignSubdomain({
      startup,
      role: roleConfig.role,
      contextWallet: base.contextWallet,
      rootWallet,
    });
    const reg8004 = await maybeRegister8004({
      contextWallet: base.contextWallet,
      startup,
      role: roleConfig.role,
      provider,
    });
    generated.push({
      ...base,
      ens,
      erc8004: reg8004,
      revokeInstructions: `Use PrismContext.revokeDelegate(${base.delegateEoa}) from root ${rootWallet.address}.`,
    });
  }

  const roleMatrix = generated.map((x) => ({
    role: x.role,
    delegateEoa: x.delegateEoa,
    contextWallet: x.contextWallet,
    ens: x.ens?.result?.assignedName || null,
    erc8004: x.erc8004?.result?.ok ? 'registered' : x.erc8004?.requested ? 'failed' : 'skipped',
    txHashes: {
      ...txHashMap(x.txs),
      ens: x.ens?.result?.ok ? x.ens?.result?.txHash : null,
      erc8004: x.erc8004?.result?.ok ? x.erc8004?.result?.txHash : null,
    },
  }));

  const artifact = {
    kind: 'startup-agent-swarm',
    startup,
    generatedAt: new Date().toISOString(),
    chainId: DEFAULT_CHAIN_ID,
    rpcUrl: DEFAULT_RPC_URL,
    rootWallet: rootWallet.address,
    prismFactoryAddress: PRISM_FACTORY_ADDRESS || null,
    prismContextAddress: PRISM_CONTEXT_ADDRESS || null,
    ensRegistryAddress: ENS_REGISTRY_ADDRESS || null,
    roles: generated,
    roleMatrix,
    safety: {
      privateKeysStoredInRepo: false,
      keyStoragePath: process.env.PRISM_KEYS_DIR || DEFAULT_KEYS_DIR,
      deterministicPolicyInput: true,
    },
    checklist: {
      minThreeAgentsGenerated: roleMatrix.length >= 3,
      hasExecute: generated.some((r) => r.txs.some((t) => t.label.endsWith(':context:execute') && t.ok)),
      hasRevoke: generated.some((r) => r.txs.some((t) => t.label.endsWith(':context:revoke') && t.ok)),
      roleMatrixPresent: roleMatrix.length > 0,
    },
  };

  await ensureDir(path.join(process.cwd(), 'agent'), 0o755);
  const outPath = path.join(process.cwd(), 'agent', `startup_swarm_${startup}.json`);
  await fs.writeFile(outPath, `${JSON.stringify(artifact, null, 2)}\n`);
  console.log(JSON.stringify({ ok: true, output: outPath, startup, roles: roleMatrix.length, roleMatrix }, null, 2));
}

main().catch((error) => {
  console.error(JSON.stringify({ ok: false, error: String(error?.message || error) }, null, 2));
  process.exitCode = 1;
});
